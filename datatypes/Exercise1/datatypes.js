// Exercise 1: Numbers

// Declare a number variable and assign it a value

const myNumber = 42; // Assign any value to 'myNumber'

// Declare a number variable and assign it a floating-point number:

const myFloat = 3.14; // Assign any floating-point value to 'myFloat'

// Perform arithmetic operations:

const num3 = 10;
const num4 = 5;

const sum = num3 + num4; // Addition
const difference = num3 - num4; // Subtraction
const product = num3 * num4; // Multiplication
const quotient = num3 / num4; // Division
const remainder = num3 % num4; // Modulus
const power = Math.pow(num3, num4); // Exponentiation

console.log("Sum:", sum);
console.log("Difference:", difference);
console.log("Product:", product);
console.log("Quotient:", quotient);
console.log("Remainder:", remainder);
console.log("Power:", power);

// Print all variables:

console.log("myNumber:", myNumber);
console.log("myFloat:", myFloat);


